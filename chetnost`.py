print("Введите 3 числа: ")
a = int(input())
b = int(input())
c = int(input())
if a % 2 == 0:
    print(f"{a} - четное")
else:
    print(f"{a} - нечетное")
if b % 2 == 0:
    print(f"{b} - четное")
else:
    print(f"{b} - нечетное")
if c % 2 == 0:
    print(f"{c} - четное")
else:
    print(f"{c} - нечетное")